package com.bridgelab;

public class telephoneentry {
	
	 String name;
	    String number;

	    public telephoneentry(String name, String number) {
	       this.name = name;
	       this.number = number;
	    }

	    public String getNumber() {
	        return number;
	    }

	    public String getName() {
	        return name;
	    }
	

}
